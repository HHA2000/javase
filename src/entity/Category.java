package entity;

/**
 *
 * @author wizard
 */
public class Category {
    private String name;
    private int id;

    public Category(int id, String name) {
        this.name = name;
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }
    
}
